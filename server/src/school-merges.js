function normalizeUnitName(value) {
  return String(value || '').replace(/\s+/g, '').trim();
}

const mergeLookup = new Map();
for (const group of SCHOOL_MERGE_GROUPS) {
  mergeLookup.set(normalizeUnitName(group.center.unitName), {
    mergeCenter: group.center.unitName,
    isCenter: true,
    schoolCode: group.center.schoolCode,
  });
  for (const member of group.members) {
    mergeLookup.set(normalizeUnitName(member.unitName), {
      mergeCenter: group.center.unitName,
      isCenter: false,
      schoolCode: member.schoolCode,
    });
  }
}

function resolveSchoolMerge(unitName) {
  return mergeLookup.get(normalizeUnitName(unitName)) || null;
}

function allMergeSchools() {
  return SCHOOL_MERGE_GROUPS.flatMap((group) => [
    { ...group.center, mergeCenter: group.center.unitName, isCenter: true },
    ...group.members.map((member) => ({
      ...member,
      mergeCenter: group.center.unitName,
      isCenter: false,
    })),
  ]);
}

